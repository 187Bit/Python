# Rollercoaster example

print("Welcome to the rollercoaster!") #print a welcome message
height = int(input("What is your height in cm?\n")) #Asks the user for the height and saves it in the variable height as a datatype int

if height >= 120: #checks if the height of the user is greater that 120
    print("You are allowed to ride the rollercoaster!") #everything that is intended belong to the code block of the if statement
else: #if the height is less than 120
    print("You are not allowed to ride the rollercoaster!") #everything that is intended belong to the code block of the else statement
 
